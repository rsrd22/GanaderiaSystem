    private void Guardar() {
        if (vistaGeneral.getOpcion() == 1) {
            vistaRefMuertes.GuardarAnulacion(modelo);
        }
        if (vistaGeneral.getOpcion() == 2) {
            vistaRefVentas.GuardarAnulacion(modelo);
        }
        ((VistaGeneral) vistaGeneral.getFrameVentana()).dispose();
    }

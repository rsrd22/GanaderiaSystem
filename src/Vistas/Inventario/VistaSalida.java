    private void AgregarSalida() {
        //<editor-fold defaultstate="collapsed" desc="LIBRO DIARIO">
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Calendar fecha = jdFecha.getCalendar();
        modeloLibro.setCantidad(txtCantidad.getText().replace(".", "").replace(",", "."));
        modeloLibro.setDebe("0");
        modeloLibro.setDetalle(Utilidades.CodificarElemento(txtProducto.getText().trim()));
        modeloLibro.setFecha("NOW()");
        modeloLibro.setFecha_libro(sdf.format(fecha.getTime()));
        modeloLibro.setHaber("0");
        modeloLibro.setId("0");
        modeloLibro.setId_finca(idFinca);
        modeloLibro.setId_usuario(datosUsuario.datos.get(0).get("ID_USUARIO"));
        modeloLibro.setPrecioxunidad("0");
        modeloLibro.setSaldo("0");
        modeloLibro.setId_producto(id_producto);
//</editor-fold>

        //<editor-fold defaultstate="collapsed" desc="SALIDA">
        modeloSalida.setFecha_salida(sdf.format(fecha.getTime()));
        modeloSalida.setFecha("NOW()");
        modeloSalida.setCantidad(txtCantidad.getText().replace(".", "").replace(",", "."));
        modeloSalida.setId_finca(idFinca);
        modeloSalida.setId_producto(id_producto);
        modeloSalida.setId_usuario(datosUsuario.datos.get(0).get("ID_USUARIO"));
        modeloSalida.setObservacion(Utilidades.CodificarElemento(txtObservacion.getText().trim()));
//</editor-fold>
        
        int retSalida = control.GuardarSalida(modeloSalida);
        if (retSalida != Retorno.EXITO) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error al momento de guardar la salida del producto.");
            return;
        }
        
        int retInventario = control.ActualizarSalidaInventario(modeloLibro);
        if (retInventario != Retorno.EXITO) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error al momento de actualizar el inventario.");
            return;
        }

        JOptionPane.showMessageDialog(null, "La operaci√≥n se realizo con exito");
        vistaInventario.AccionCombo();
        vistaGeneral.dispose();
    }

    private void cargarProducto(Map<String, String> producto) {
        id_producto = producto.get("id_producto");
        idFinca = producto.get("id_finca");
        txtProducto.setText(producto.get("PRODUCTO"));
        txtProducto.setEnabled(false);
        txtCantidad.setText("");
        jdFecha.setCalendar(Calendar.getInstance());
    }

